package model;

public class Enemy extends Ecommand{

	private String name;
	private int hp;
	private int power;
	private int kcal;
	private String dropItem;

	public Enemy() {}

	public Enemy(Enemy enemy) {
		this.getName();
		this.getHp();
		this.getKcal();
		this.getPower();
		this.getDropItem();
	}

	public Enemy(FoodEnemyBean enemy) {
		this.getName();
		this.getHp();
		this.getKcal();
		this.getDropItem();
	}
	public Enemy(ManEnemyBean enemy) {
		this.getName();
		this.getHp();
		this.getPower();
		this.getDropItem();
	}
	public String getName() {
		return name;
	}
	public int getPower() {
		return power;
	}
	public int getKcal() {
		return kcal;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getDropItem() {
		return dropItem;
	}
	public int getHp() {
		return hp;
	}
	public void setHp(int hp) {
		this.hp = hp;
	}


}
