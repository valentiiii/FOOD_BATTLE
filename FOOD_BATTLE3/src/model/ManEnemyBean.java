package model;

import java.io.Serializable;

public class ManEnemyBean extends Enemy implements Serializable{
	/**
	 *
	 */
	private static final long serialVersionUID = 1L;
	private String name;
	private int hp;
	private int power;
	private String dropItem;

	public ManEnemyBean(){}
	public ManEnemyBean(String name , int hp , int power,String dropItem){
		this.name = name;
		this.hp = hp;
		this.power = power;
		this.dropItem=dropItem;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getHp() {
		return hp;
	}
	public void setHp(int hp) {
		this.hp = hp;
	}
	public int getPower() {
		return power;
	}
	public String getDropItem() {
		return dropItem;
	}
}